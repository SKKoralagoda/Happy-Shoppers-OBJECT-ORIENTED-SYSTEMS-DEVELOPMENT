    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        String id = txtitemno.getText();
         try {       
            FileInputStream fin = new FileInputStream ("cart.txt");
            ObjectInputStream ois = new ObjectInputStream(fin);
            ArrayList<ShoppingCart> cartlist = new ArrayList<>();
            cartlist =  (ArrayList<ShoppingCart>) ois.readObject();
            
            
            fin.close();
            for(int i = 0; i < cartlist.size() ; i++){
                if(id.equals(cartlist.get(i).getPid()) && username.equals(cartlist.get(i).getUserid())){
                    ShoppingCart ob = cartlist.get(i);
                    cartlist.remove(ob);
                    JOptionPane.showMessageDialog(this, "succesfully removed", "Cart status", 1);
                }else{
                    JOptionPane.showMessageDialog(this, "Please enter a valid id", "cart status", 0);
                }
            }
            FileOutputStream fos = new FileOutputStream("cart.txt");
                ObjectOutputStream oos = new ObjectOutputStream(fos);
                oos.writeObject(cartlist);
                oos.close();
        } catch (FileNotFoundException ex) {
        
        } catch (IOException | ClassNotFoundException ex) {
           
        } 
    }//GEN-LAST:event_btnremoveActionPerformed

