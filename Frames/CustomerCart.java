    public final void filltable(){
        try {
            FileInputStream fin = new FileInputStream ("cart.txt");
            ObjectInputStream ois = new ObjectInputStream(fin);
            ArrayList<ShoppingCart> cartlist = new ArrayList<>();
            cartlist = (ArrayList<ShoppingCart>) ois.readObject();
            
            FileInputStream fine = new FileInputStream ("product.txt");
            ObjectInputStream oise = new ObjectInputStream(fine);
            ArrayList<Product> productlist = new ArrayList<>();
            productlist = (ArrayList<Product>) oise.readObject();
            
            DefaultTableModel model = (DefaultTableModel) tblcart.getModel();
            Object rowData[] = new Object[3];
            for(int i = 0; i < cartlist.size(); i++){
                if(username.equals(cartlist.get(i).getUserid())){
                    String pid = cartlist.get(i).getPid();
                    for(int a = 0; a < productlist.size(); a++){
                        if(pid.equals(productlist.get(a).productId)){
                            rowData[0] = productlist.get(a).productId;
                            rowData[1] = productlist.get(a).productName;
                            rowData[2] = productlist.get(a).price;
                            model.addRow(rowData);
                        }
                    }
                }
          
            }
        } catch (FileNotFoundException ex) {
            
        } catch (IOException | ClassNotFoundException ex) {
            
        }
    }
